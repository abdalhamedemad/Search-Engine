public class Pair {
    String str;
    int num;

    public Pair(String str, int num) {
        this.str = str;
        this.num = num;
    }

    public void setStr(String str) {
        this.str = str;
    }

    public void setNum(int num) {
        this.num = num;
    }

    public String getStr() {
        return str;
    }

    public int getNum() {
        return num;
    }
}
